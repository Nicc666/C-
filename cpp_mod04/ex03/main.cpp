#include "Character.hpp"
#include "ICharacter.hpp"
#include "Ice.hpp"
#include "Cure.hpp"

int main()
{
	ICharacter* me = new Character("me");
	AMateria* tmp;
	tmp = new Ice();
	me->equip(tmp);
	tmp = new Cure();
	me->equip(tmp);
	ICharacter* bob = new Character("bob");
	me->use(0, *bob);
	me->use(1, *bob);
	delete bob;
	delete me;
	return (0);
}
